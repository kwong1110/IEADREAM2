package idealType.model.vo;

public class Stat {

	private String item;
	private double prop;
	
	public Stat() {
		super();
	}

	public Stat(String item, double prop) {
		super();
		this.item = item;
		this.prop = prop;
	}

	public String getItem() {
		return item;
	}

	public void setItem(String item) {
		this.item = item;
	}

	public double getProp() {
		return prop;
	}

	public void setProp(double prop) {
		this.prop = prop;
	}
	
	
	
	
}
